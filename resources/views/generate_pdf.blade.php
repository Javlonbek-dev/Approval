<p class="paragraph-P12"><span class="text-T8">8. SHARTNOMANING AMAL QILISH MUDDATI</span></p>
<p class="paragraph-P23"> </p>
<p class="paragraph-P14"><span class="text-T17">8.1. Mazkur shartnoma 2 (ikki) nusxada tuzilib, taraflar tomonidan imzolangan vaqtdan boshlab kuchga kiradi va  31.12.2024 yilga qadar amal qiladi.</span>
</p>

<p class="paragraph-P14"><span class="text-T17">8.2.  Taraflarning har biri ushbu shartnomani amalda bo‘lgan davrida bekor qilishlik uchun 30 kun oldin yozma ravishda xabardor qiladi va ushbu shartnomani muddatidan oldin bekor qilish uchun taraflarning o‘zaro kelishuvi asosida, o‘zaro kelishuvga erishilmagan taqdirda, sud orqali hal etiladi. Shartnoma bir tomonlama bekor qilinishiga yo‘l qo‘yilmaydi.</span>
</p>
<p class="paragraph-P41"> </p>
<p class="paragraph-P12"><span class="text-T10">9</span><span class="text-T8">. </span><span class="text-T10">YAKUNIY QOIDALAR</span>
</p>
