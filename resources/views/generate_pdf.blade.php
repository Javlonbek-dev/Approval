</p>

<p class="paragraph-P14"><span class="text-T6">9.2. “Buyurtmachi” “Bajaruvchi”ga Ma’qullash bo‘yicha baholash o‘tkazishda, kerakli shart-sharoitlarni yaratib berishi shart.</span>
</p>

<p class="paragraph-P14"><span class="text-T6">9.3. Shartnoma bekor qilinganida shartnoma yuzasidan haqiqiy harajatlar bo‘yicha o‘zaro hisob-kitob qilinadi.</span>
</p>

<p class="paragraph-P14"><span class="text-T11">9</span><span class="text-T6">.</span><span
