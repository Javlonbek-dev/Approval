<p class="paragraph-P12"><span class="text-T8">5. NIZOLARNI HAL ETISH TARTIBI</span></p>
<p class="paragraph-P17"> </p>
<p class="paragraph-P14"><span class="text-T6">5.1. Agar “Buyurtmachi” ko‘rsatilgan xizmatlarga nisbatan e’tiroz yoki shikoyati bilan murojaat qilsa, “Buyurtmachi” ularni “Bajaruvchi” ga O‘zbekiston Respublikasining amaldagi qonunchiligda belgilangan muddatlarda bildirishga haqlidir.</span>
</p>

<p class="paragraph-P14"><span class="text-T6">5.2. Taraflar nizoni sudgacha hal qilish (talabnoma yuborish) tartibiga amal qilib, o‘zaro kelishuvga erishmagan taqdirda, kelishmovchilik va nizolar javobgar joylashgan tumanlararo iqtisodiy sudida O‘zbekiston Respublikasining amaldagi qonunchiligi bo‘yicha hal qilinadi.</span>
</p>

<p class="paragraph-P14"><span class="text-T6">5.3. “Buyurtmachi” ning “Bajaruvchi” xodimlarining hatti-harakatlari yuzasidan shikoyatlari “Bajaruvchi” direktori nomiga yozma ravishda kiritiladi. </span>
</p>
<p class="paragraph-P20"> </p>
<p class="paragraph-P12"><span class="text-T8">6. FORS-MAJOR VAZIYATLAR</span></p>
<p class="paragraph-P18"> </p>
<p class="paragraph-P14"><span class="text-T6">6.1. Agar fors-major holati oqibatida Tomonlar mazkur shartnomada qayd etilgan o‘z majburiyatlaridan istalgan birini to‘liq yoki qisman bajara olmasa, buning uchun tomonlarning birortasi javobgar bo‘lmaydi. </span>
</p>

<p class="paragraph-P14"><span class="text-T6">6.2. Tabiiy ofatlar, urush, qamal, sabotaj, jamoat tartibsizliklari, ish tashlashlar, eksport yoki importga taqiq qo‘yilishi, karantin va epidemiyalar, Tomonlarga bog‘liq bo‘lmagan davlat organlarining harakatlari yoki harakatsizligi, Tomonlarning mazkur shartnoma bo‘yicha o‘z majburiyatlarining bajarishini imkonsiz qilib qo‘yadigan normativ-huquqiy hujjatlarning qabul qilinishi, shuningdek qonunchilikka muvofiq tomonlar oldindan ko‘ra olish yoki oldini olishi mumkin bo‘lmagan boshqa xodisalar fors-major hisoblanadi. </span>
</p>

<p class="paragraph-P14"><span class="text-T6">6.3. Fors-major yuzaga kelganda, mazkur Shartnoma bo‘yicha o‘z majburiyatlarini bajarishiga imkon bermaydigan holat sodir bo‘lgan Tomon zudlik bilan (bunday holat sodir bo‘lgan kundan boshlab 3 kundan kechiktirmay) ikkinchi Tomonni fors-major yuzaga kelgani haqida yozma ravishda xabardor qiladi hamda ushbu holatlar sodir bo‘lib turgan vaqtda Shartnoma bo‘yicha o‘z majburiyatlarini bajarishni kechiktirishni taklif qiladi.     </span>
</p>

<p class="paragraph-P14"><span class="text-T6">6.4. Agar fors-major 3 oydan ortiq davom etsa, istalgan Tomon mazkur Shartnoma bo‘yicha o‘z majburiyatlarini bajarishni davom ettirishdan voz kechishga haqli. </span>
</p>
<p class="paragraph-P40"> </p>
<p class="paragraph-P12"><span class="text-T8">7. TOMONLARNING KORRUPSIYAGA QARSHI CHORALARI</span></p>
<p class="paragraph-P22"> </p>
<p class="paragraph-P15"><span class="text-T17">7.1. Tomonlar ularning affillangan shaxslari, xodimlari yoki boshqa ishtirokchilari ushbu shartnoma shartlarini bajarishda ikkinchi tomonning qaror qabul qilishiga ta’sir o‘tkazish maqsadida bevosita yoki bilvosita pul mablag‘lari, qimmatliklar yoki xizmatlar taklif qilishi, berishi, taqdim etishi taqiqlanadi.</span>
</p>

<p class="paragraph-P15"><span class="text-T17">7.2. Tomonlar, ularning affillangan shaxslari, xodimlari yoki boshqa ishtirokchilari ushbu shartnoma shartlarini bajarishda qaror qabul qilishiga ta’sir o‘tkazish maqsadida shartnomaning boshqa ishtirokchilari tomonidan bevosita yoki bilvosita pul mablag‘lari, qimmatliklar yoki xizmatlar taqdim etish bo‘yicha taklifini rad qilishi lozim.</span>
</p>

<p class="paragraph-P15"><span class="text-T17">7.3. Agar shartnoma ishtirokchilarida ushbu shartnoma shartlarini bajarish bilan bog‘liq korrupsion harakat sodir etilganligi to‘g‘risida ma’lumot, shubhaga asos bo‘luvchi dalilga ega bo‘lsa, bu to‘g‘risida ikkinchi tomonga yozma shaklda xabar berish majburiyatini oladi.</span>
</p>

<p class="paragraph-P15"><span class="text-T17">7.4. Xabarni olgan tomon haqiqatda korrupsion harakat sodir etilmaganligini yoki sodir etilmasligi to‘g‘risida tasdiqqa ega bo‘lgunicha ushbu shartnoma bo‘yicha o‘z majburiyatlarini bajarishini to‘xtatib turish huquqiga ega.</span>
</p>

<p class="paragraph-P15"><span class="text-T17">7.5. Pora berish evaziga ikkinchi tomon vakilini, affillangan shaxsini, xodimini yoki shartnomaning boshqa ishtirokchilarini shartnoma shartlarini bajarishda o‘ziga og‘dirib olgan tomon ikkinchi tomonga yetkazilgan zararlarni qoplab berish majburiyatini oladi.</span>
</p>
<p class="paragraph-P41"> </p>
<p class="paragraph-P41"> </p>
